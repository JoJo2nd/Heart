	void hZipFile::SeekInternal( unzFile zip )
	{
		hUint64 size = size_;
		switch ( opData_.where_ )
		{
		case SEEKOFFSET_BEGIN:
			{
				filePos_ = opData_.seek_;
			}
			break;
		case SEEKOFFSET_CURRENT:	
			{
				filePos_ += opData_.seek_;
			}
			break;
		case SEEKOFFSET_END:
			{
				filePos_ = size;
				filePos_ += opData_.seek_;
			}
			break;
		}

		if ( filePos_ > size )
		{
			filePos_ = size;
		}

		nextOP_ = 0;
	}

    //////////////////////////////////////////////////////////////////////////
