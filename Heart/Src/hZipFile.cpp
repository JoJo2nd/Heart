    hUint32 hZipFile::Read( void* pBuffer, hUint32 size )
    {
        hUint32 ret;
        if ( !ReadAsync( pBuffer, size ) )
            return 0;
        while ( !IsDone( &ret ) ) { Heart::Threading::ThreadSleep( 1 ); }
        return ret;
    }

    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////

    hUint32 hZipFile::Write( const void* pBuffer, hUint32 size )
    {
        hUint32 ret;
        if ( !WriteAsync( pBuffer, size ) )
            return 0;
        while ( !IsDone( &ret ) ) { Heart::Threading::ThreadSleep( 1 ); }
        return ret;
    }

    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////

    hUint32 hZipFile::Seek( hUint64 offset, SeekOffset from )
    {
        hUint32 ret;
        if ( !SeekAsync( offset, from ) )
            return 0;
        while ( !IsDone( &ret ) ) { Heart::Threading::ThreadSleep( 1 ); }
        return ret;
    }
