gdDECLARE_DLL_FUNCTION_PROTOTYPE( void, GetPlugInInformation );
gdDECLARE_DLL_FUNCTION_PROTOTYPE( GameData::gdResourceBuilderBase*, CreateResourceBuilder );
gdDECLARE_DLL_FUNCTION_PROTOTYPE( void, DestroyResourceBuilder );

namespace GameData
{

    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////

    gdPlugInInformation::gdPlugInInformation()
        : dllHandle_(NULL)
        , infoProc_(NULL)
    {
        SetBuiltDataExtension( '?', '?', 0 );
    }

    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////

    gdPlugInInformation::~gdPlugInInformation()
    {

    }

    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////

    gdBool gdPlugInInformation::Initialise( HMODULE dllHandle, gdTime_t lastModTime )
    {
        dllHandle_ = dllHandle;
        lastModTime_ = lastModTime;

        infoProc_ = gdGET_DLL_PROC_ADDRESS(dllHandle_, GetPlugInInformation );
        createProc_ = gdGET_DLL_PROC_ADDRESS(dllHandle_, CreateResourceBuilder );
        destroyProc_ = gdGET_DLL_PROC_ADDRESS(dllHandle_, DestroyResourceBuilder );

        if ( infoProc_ )
        {
            (infoProc_)( this );
        }

        return GetIsValid();
    }

    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////

    gdBool gdPlugInInformation::GetIsValid() const
    {
        gdBool valid = true;

        valid &= dllHandle_ != NULL;
        valid &= infoProc_ != NULL;
        valid &= createProc_ != NULL;
        valid &= destroyProc_ != NULL;
        valid &= !configName_.empty();
        valid &= !extensions_.empty();
        valid &= !resourceTypeName_.empty();
        valid &= isalpha( builtDataExt_[1] ) > 0;
        valid &= isalpha( builtDataExt_[2] ) > 0;
        valid &= isalpha( builtDataExt_[3] ) > 0;

        for ( gdUint i = 0; i < parameterDefinitions_.size(); ++i )
        {
            valid &= parameterDefinitions_[i].GetIsValid();
        }

        return valid;
    }

    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////

    void gdPlugInInformation::SetConfigName( const gdChar* configName )
    {
        configName_ = configName;    
    }

    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////

    void gdPlugInInformation::SetResourceTypeName( const gdChar* typeName )
    {
        resourceTypeName_ = typeName;
    }

    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////

    void gdPlugInInformation::SetDefaultExtensions( const gdChar* exts )
    {
        extensions_ = exts;
    }

    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////

    void gdPlugInInformation::AppendDefaultParameter( const gdParameterDefinition& paramDef )
    {
        parameterDefinitions_.push_back( paramDef );
    }

    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////

    const gdChar* gdPlugInInformation::GetConfigName() const
    {
        return configName_.c_str();
    }

    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////

    const gdChar* gdPlugInInformation::GetResourceTypeName() const
    {
        return resourceTypeName_.c_str();
    }

    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////

    const gdChar* gdPlugInInformation::GetDefaultExtensions() const
    {
        return extensions_.c_str();
    }

    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////

    void gdPlugInInformation::GetDefaultParameterArray( gdParameterDefinitionArray& out ) const
    {
        out = parameterDefinitions_;
    }

    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////

    gdTime_t gdPlugInInformation::GetLastModificationTime() const
    {
        return lastModTime_;
    }

    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////

    gdResourceBuilderBase* gdPlugInInformation::CreateResourceBuilder( const gdResourceBuilderConstructionInfo& info ) const
    {
        return (*createProc_)( info );
    }

    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////

    void gdPlugInInformation::DestroyResourceBuiler( gdResourceBuilderBase* builder ) const
    {
        (*destroyProc_)( builder );
    }
    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////
    //////////////////////////////////////////////////////////////////////////

    void gdPlugInInformation::SetBuiltDataExtension( gdChar a, gdChar b, gdChar c )
    {
        builtDataExt_[0] = '.';
        builtDataExt_[4] = 0;
        builtDataExt_[1] = toupper(a); builtDataExt_[2] = toupper(b); builtDataExt_[3] = toupper(c);
    }

}